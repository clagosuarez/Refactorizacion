
package factorial;


public class Factorial {


    public static void main(String[] args) {


    int jota;
    int efe;

  
   jota = 8;    
   

    
   int i;
   if (jota == 0)
efe = 1;
   else
   {
efe = 1;
for (i = jota; i >= 1; i--)
{
    efe = efe * i;
}
   }    
  
 

System.out.println(efe); 


    }
    
}
